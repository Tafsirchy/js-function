function tenTimes(num){
    const result = num * 10;
    return result;
}

function cutHalf(num){
    const result = num / 2;
    return result;
}


const output1 = tenTimes(10);
console.log(output1);

const bigNumber1 = tenTimes(99);
console.log(bigNumber1);

const output2 = cutHalf(45);
console.log(output2);

const bigNumber2 = cutHalf(999999);
console.log(bigNumber2);